   margin-left: 1rem;
 }
 This rule matches the entire nested .collection-section <div> (title and
 its content), not just the title. Because CollapsibleSection components nest
 inside one another, each level's margin-left: 1rem is relative to its already-shifted
 parent, so the indent compounds: level 2 sits 1rem in, level 3 sits 2rem in — and
 since that div's children include the eventual .products-grid, the grid inherits
 the full 2rem inset and loses that much width too (an auto-width block's content box
 shrinks by its own margin-left). With the fixed grid-template-columns: repeat(3, 1fr)
 track sizing (no minmax(0, 1fr)), that lost width is enough to push the 3rd card
 column past the viewport edge, matching the screenshot.

